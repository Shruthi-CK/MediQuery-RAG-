    '\n',
    'We mine **10 hard negatives per query** (up from 1). Cross-encoders score `(query, passage)` pairs independently \u2014 they don\u2019t benefit from in-batch negatives like bi-encoders do \u2014 so providing more explicit negatives gives a much stronger training signal. Each query produces 1 positive + 10 negative examples = 11 training examples.',
